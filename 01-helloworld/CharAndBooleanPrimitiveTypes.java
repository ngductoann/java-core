public class CharAndBooleanPrimitiveTypes {
    public static void main(String[] args) {
        char myChar = 'D';
        System.out.println(myChar);

        String myString = "Hello world";
        System.out.println(myString);

        myChar = '\u0044';
        System.out.println(myChar);

        myChar = 68;
        System.out.println(myChar);
    }
}
